    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed

        if (jTextField13.getText().isEmpty()) {
            jLabel40.setText("<html><font color=\"red\">* No Urut Pembelajaran tidak boleh kosong</font</html>");
        } else {
            jLabel40.setText("");
            if (jTextField14.getText().isEmpty()) { 
                jLabel41.setText("<html><font color=\"red\">* Nama Pembelajaran tidak boleh kosong</font</html>");
            } else {
                jLabel41.setText("");
                if (jFormattedTextField1.getText().isEmpty()) {
                    jLabel42.setText("<html><font color=\"red\">* Biaya Pembelajaran tidak boleh kosong</font</html>");
                } else {
                    jLabel42.setText("");
                    if (jTextArea1.getText().isEmpty()) {
                        jLabel43.setText("<html><font color=\"red\">* Deskripsi Pembelajaran tidak boleh kosong</font</html>");
                    } else {
                        jLabel43.setText("");
                        int input = JOptionPane.showConfirmDialog(null,
                                "Yakin ingin simpan pembelajaran baru?", "Lanjut simpan",JOptionPane.YES_NO_OPTION);
                        if (input == 0) {
                            System.out.println("Anda pilih yes");
                            tpaAzkaAiniya.savePembelajaran();
                            if (TPAAzkaAiniya.isIfTrueForCheck()) {
                                jFormattedTextField1.setText("");
                                jTextField13.setText("");
                                jTextField14.setText("");
                                jTextArea1.setText("");
                                try {
                                    tpaAzkaAiniya.kodePembelajaranTesting();
                                } catch (SQLException ex) {
                                    Logger.getLogger(Pembelajaran.class.getName()).log(Level.SEVERE, null, ex);
                                }
                            }
                            TPAAzkaAiniya.setIfTrueForCheck(false);
                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_jButton8ActionPerformed

